namespace librarycalculator
{
    public class Calculator
    {
       public   int toplama(int num1,int num2)
        {
            return num1 + num2;   
        }
        public  int cixma(int num1,int num2)
        {
            return num1 - num2;
        }
        public double bolme(double num1,double num2)
        {
            return (num1 / num2);
        
        }
        public int vurma(int num1,int num2)
        {
            return num1 * num2;
        }
        

    }
}